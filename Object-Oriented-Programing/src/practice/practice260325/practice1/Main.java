//자바 API 사용 예제 모아보기
package practice.practice260325.practice1;

public class Main {
    public static void main(String[] args) {
        String text = "Hello Java";

        System.out.println(text.length()); //문자열 길이
        System.out.println(text.toLowerCase()); //대문자 변환
        System.out.println(text.toUpperCase()); //소문자 변환
        System.out.println(Math.abs(-10)); //절댓값
        System.out.println(Math.max(3,7)); //큰 값
    }
}
